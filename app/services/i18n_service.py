_LANGUAGE_OVERRIDES: Dict[str, Dict[str, str]] = {
    "ko": _MESSAGES_KO,
    "ja": _MESSAGES_JA,
    "zh-cn": _MESSAGES_ZH_CN,
    "zh-tw": _MESSAGES_ZH_TW,
    "es": _MESSAGES_ES,
    "fr": _MESSAGES_FR,
    "de": _MESSAGES_DE,
    "pt-br": _MESSAGES_PT_BR,
    "hi": _MESSAGES_HI,
}


def _canonical_lang(language_code: str | None) -> str:
    code = (language_code or "").strip().lower().replace("_", "-")
    if not code or code == "auto":
        return "en"
    if code.startswith("ko"):
        return "ko"
    if code.startswith("ja"):
        return "ja"
    if code.startswith("zh-tw") or code.startswith("zh-hk") or code.startswith("zh-hant"):
        return "zh-tw"
    if code.startswith("zh"):
        return "zh-cn"
    if code.startswith("es"):
        return "es"
    if code.startswith("fr"):
        return "fr"
    if code.startswith("de"):
        return "de"
    if code.startswith("pt"):
        return "pt-br"
    if code.startswith("hi"):
        return "hi"
    return "en"


def get_i18n_messages(language_code: str | None) -> Dict[str, str]:
    lang = _canonical_lang(language_code)
    merged = dict(_MESSAGES_EN)
    overrides = _LANGUAGE_OVERRIDES.get(lang)
    if overrides:
        merged.update(overrides)
    return merged
